export const convertMazeData = (): CellType[][] => {
  return MAZE_DATA.map(row => 
    row.map(cell => {
      switch(cell) {
        case 0: return 'wall';
        case 1: return 'dot';
        case 2: return 'empty';
        case 3: return 'powerPellet';
        default: return 'wall';
      }
    })
  );
};

